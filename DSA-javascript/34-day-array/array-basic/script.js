//Array BAics
let arr = [10,20,30,40,50];
console.log(arr);
//push(val) => add elem at last
arr.push(60);
console.log(arr);
//pop() => remove last elem
let a = arr.pop();
console.log(a);
console.log(arr);

//unshift(val)=> insert at first
arr.unshift(100);
console.log(arr);

//shift()=> delete at first

let b = arr.shift();
console.log(b);
console.log(arr);